using ContosoUniversity.Models;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;

namespace ContosoUniversity.Controllers
{
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;

        public HomeController(ILogger<HomeController> logger)
        {
            _logger = logger;
        }

        // IActionResult is an interface that defines something that represents the result of an action
        // Index() is the default method called as an HTTP endpoint
        public IActionResult Index()
        {
            return View();
        }

        // Returns a view locate in the View/Home directory
        // No view is specified so the default view is used, which is the one with the same name as the action method
        public IActionResult Privacy(string first = "daniel", string last = "androussenko", string email = "dandrous@uw.edu")
        {

            ViewData["FirstName"] = first;
            ViewData["LastName"] = last;
            ViewData["EmailAddress"] = email;

            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()
        {
            return View(new ErrorViewModel { RequestId = Activity.Current?.Id ?? HttpContext.TraceIdentifier });
        }


    }
}
